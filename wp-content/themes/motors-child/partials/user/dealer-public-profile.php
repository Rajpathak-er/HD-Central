function convertYoutube($string) {
	return preg_replace(
		"/\s*[a-zA-Z\/\/:\.]*youtu(be.com\/watch\?v=|.be\/)([a-zA-Z0-9\-_]+)([a-zA-Z0-9\/\*\-\_\?\&\;\%\=\.]*)/i",
		"<iframe width=\"720\" height=\"460\" src=\"//www.youtube.com/embed/$2\" allowfullscreen></iframe>",
		$string
	);
}

function getYoutubeEmbedUrl($url, $number=0) {
	if (str_contains($url, 'facebook')) { 
		$final_html = '<div id="fb-root"></div>';
		$final_html .= '<script async defer src="https://connect.facebook.net/en_US/sdk.js#xfbml=1&version=v3.2"></script>';

		$final_html .= '<div class="fb-video" data-href="'.$url.'" data-width="500" data-show-text="false">';
		$final_html .= '<div class="fb-xfbml-parse-ignore">';
		$final_html .= '<blockquote cite="'.$url.'">';
		$final_html .= '</blockquote>';
		$final_html .= '</div>';
		$final_html .= '</div>';
	} else {
		$shortUrlRegex = '/youtu.be\/([a-zA-Z0-9_-]+)\??/i';
		$longUrlRegex = '/youtube.com\/((?:embed)|(?:watch)|(?:shorts))((?:\?v\=)|(?:\/))([a-zA-Z0-9_-]+)/i';

		if (preg_match($longUrlRegex, $url, $matches)) {
			$youtube_id = $matches[count($matches) - 1];
		}

		if (preg_match($shortUrlRegex, $url, $matches)) {
			$youtube_id = $matches[count($matches) - 1];
		}

		$embed_url = 'https://www.youtube.com/embed/' . $youtube_id;
		#$final_html = '<div style="visibility: hidden;">'.$embed_url.'</div>';
		$final_html = '<iframe width="720" height="460" src="'.$embed_url.'" allowfullscreen></iframe>';
	}
	return $final_html;
}



